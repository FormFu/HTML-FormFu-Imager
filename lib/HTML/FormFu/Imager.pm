package HTML::FormFu::Imager;

use strict;

our $VERSION = '0.01001';
$VERSION = eval $VERSION;

1;

=head1 NAME

HTML::FormFu::Imager - Imager.pm helpers for HTML::FormFu file uploads

=head1 DESCRIPTION

L<HTML::FormFu::Inflator::Imager> - inflate file uploads to L<Imager> 
objects.

L<HTML::FormFu::Transformer::Imager> - perform image transformations, such 
as scale, crop and rotate.

L<HTML::FormFu::Validator::Imager::Size> - ensure the image doesn't exceed a 
given pixel size.

